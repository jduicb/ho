import questionnairesModel from "../models/QuestionnairesModel.js";

const QuestionnairesController = {
  getAll: (req, res) => {
    questionnairesModel.getAllQuestionnaires((err, data) => {
      if (err) return res.status(500).json({ error: err.message });
      res.json(data);
    });
  },
  getById: (req, res) => {
    questionnairesModel.getQuestionnaireById(req.params.id, (err, data) => {
      if (err) return res.status(500).json({ error: err.message });
      if (!data) return res.status(404).json({ message: "Not found" });
      res.json(data);
    });
  },
  add: (req, res) => {
    questionnairesModel.addQuestionnaire(req.body, (err, result) => {
      if (err) return res.status(500).json({ error: err.message });
      res.json({ message: "Added", result });
    });
  },
  update: (req, res) => {
    questionnairesModel.updateQuestionnaire(req.params.id, req.body, (err, result) => {
      if (err) return res.status(500).json({ error: err.message });
      res.json({ message: "Updated", result });
    });
  },
  delete: (req, res) => {
    questionnairesModel.deleteQuestionnaire(req.params.id, (err) => {
      if (err) return res.status(500).json({ error: err.message });
      res.json({ message: "Deleted" });
    });
  },
};
export default QuestionnairesController;
